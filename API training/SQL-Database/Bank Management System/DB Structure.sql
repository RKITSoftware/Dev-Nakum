CREATE DATABASE BANK;
USE BANK;

-- create customer table 
CREATE TABLE
	CUS01(
    S01F01 INT PRIMARY KEY AUTO_INCREMENT,
    S01F02 VARCHAR(20) NOT NULL,
    S01F03 VARCHAR(20) NOT NULL,
    S01F04 BIGINT  UNIQUE NOT NULL,
    S01F05 VARCHAR(50) 
);


-- create account table 
CREATE TABLE
	ACC01(
    C01F01 INT PRIMARY KEY AUTO_INCREMENT,
    C01F02 VARCHAR(10)  NOT NULL,
    C01F03 INT  NOT NULL,
    C01F04 INT REFERENCES CUS01(S01F01) 
);


-- create transaction table
CREATE TABLE
	TRA01(
    A01F01 INT PRIMARY KEY AUTO_INCREMENT,
    A01F02 VARCHAR(10) NOT NULL,
    A01F03 DATETIME NOT NULL,
	A01F04 INT NOT NULL,
	A01F05 INT REFERENCES ACC01(C01F01) 
);