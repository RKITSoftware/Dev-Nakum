USE employee;

-- Creating 'employees' table
CREATE TABLE EMP02 (
    P02F01 INT COMMENT "Id" PRIMARY KEY AUTO_INCREMENT,
    P02F02 VARCHAR(50) COMMENT "Name",
    P02F03 INT COMMENT "department_id",
    FOREIGN KEY (P02F03) REFERENCES DEP01(P01F01)
) COMMENT "Employee 2";

-- Creating 'departments' table
CREATE TABLE DEP01 (
    P01F01 INT PRIMARY KEY AUTO_INCREMENT COMMENT "Id",
    P01F02 VARCHAR(50) COMMENT "Name"
) COMMENT "Department";

