-- INNER JOIN 
SELECT 
	P02F01,
    P02F02,
    P01F02 
FROM 
	EMP02 
INNER JOIN 
	DEP01 
ON 
	EMP02.P02F03 = DEP01.P01F01;
    
    
-- LEFT JOIN 
SELECT 
	P02F01,
    P02F02,
    P01F02 
FROM 
	EMP02 
LEFT JOIN 
	DEP01 
ON 
	EMP02.P02F03 = DEP01.P01F01;
    
    
-- RIGHT JOIN 
SELECT 
	P02F01,
    P02F02,
    P01F02 
FROM 
	EMP02 
RIGHT JOIN 
	DEP01 
ON 
	EMP02.P02F03 = DEP01.P01F01;
    
-- FULL JOIN 
SELECT 
    P02F01,
    P02F02,
    P01F02 
FROM 
    EMP02
FULL JOIN 
    DEP01 
ON 
    P02F03 = P01F01;
    
    
-- SELF JOIN
SELECT 
    E1.P02F01,
    E1.P02F02,
    E1.P02F03
FROM 
    EMP02 E1
JOIN
	EMP02 E2
WHERE 
	E1.P02F01 <> E2.P02F01
AND
	E1.P02F03 = E2.P02F03